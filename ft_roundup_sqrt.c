#include "fillit.h"

int		ft_roundup_sqrt(int nb)
{
	int		a;

	a = 0;
	if (!nb || nb < 0 || nb > 2147395600)
		return (0);
	while (a * a < nb)
		a += 1;
	return (a);
}
